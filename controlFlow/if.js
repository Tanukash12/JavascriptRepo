// if, if-else, elif,
let age = 20;

if (age < 18) {
  console.log("Minor");
} else if (age >= 18 && age < 60) {
  console.log("Adult");
} else {
  console.log("Senior Citizen");
}


// Switch case
let day = "Monday";

switch (day) {
  case "Monday":
    console.log("Start of the week!");
    break;
  case "Friday":
    console.log("Almost weekend!");
    break;
  default:
    console.log("Just another day.");
}


